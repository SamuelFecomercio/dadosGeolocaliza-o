# Função para separar endereço em bairro e cidade
def separar_endereco(lista):
    for item in lista:
        endereco = item["endereco"]
        partes_endereco = endereco.split(", ")
        bairro = partes_endereco[-2]
        cidade = partes_endereco[-1]
        item["bairro"] = bairro
        item["cidade"] = cidade
        item["endereco"] = partes_endereco[0]  # Removendo bairro e cidade do endereço

# Ordenar a lista de objetos de acordo com a ordem desejada
def ordenar_dados(lista):
    for item in lista:
        item_keys = list(item.keys())
        sorted_keys = ["id", "fantasia", "endereco", "bairro", "cidade", "latitude", "longitude"]
        ordered_item = {key: item[key] for key in sorted_keys if key in item_keys}
        item.clear()
        item.update(ordered_item)

